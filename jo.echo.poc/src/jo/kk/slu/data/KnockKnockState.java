package jo.kk.slu.data;

public class KnockKnockState
{
    private String[]    mJoke;
    private int         mCall;
    
    public String[] getJoke()
    {
        return mJoke;
    }
    public void setJoke(String[] joke)
    {
        mJoke = joke;
    }
    public int getCall()
    {
        return mCall;
    }
    public void setCall(int call)
    {
        mCall = call;
    }
}
